// Hàm helper để lấy bản dịch
export const getTranslatedPostContent = (title, field, language) => {
  if (language === "Vietnamese" || !postContentTranslations[language]) {
    return null; // Không có bản dịch, dùng nguyên văn
  }
  
  if (!title) return null;
  
  // Trim title để loại bỏ khoảng trắng đầu/cuối
  const trimmedTitle = title.trim();
  
  // Thử tìm với title đã trim
  let translation = postContentTranslations[language][trimmedTitle];
  
  // Nếu không tìm thấy với trimmed, thử với title gốc (có thể có khoảng trắng)
  if (!translation) {
    translation = postContentTranslations[language][title];
  }
  
  if (!translation) {
    return null; // Không có bản dịch cho bài post này
  }
  
  return translation[field]; // field là 'title' hoặc 'description'
};

